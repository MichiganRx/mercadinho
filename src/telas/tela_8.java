     public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) txtLogin.getModel();
        modelo.setNumRows(0);
        FuncionarioDao pdao = new FuncionarioDao();

        for (Funcionario p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getid(),
                p.getnome(),
                p.getemail(),
                p.getlogin(),
                p.getsenha(),
            });
        }
    }
